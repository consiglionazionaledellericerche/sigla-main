			AND mr.CD_TIPO_DOCUMENTO_AMM = 'FATTURA_P'
			AND fp.DT_CANCELLAZIONE IS NULL
			UNION ALL
			SELECT fp.ID_PAESE, fp.ID_CODICE, fp.IDENTIFICATIVO_SDI, fp.PROGRESSIVO, fpr.IM_IMPONIBILE IMPONIBILE_IMPORTO, fpr.IM_IVA IMPOSTA,
