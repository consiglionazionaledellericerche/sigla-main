				   NULL DT_EMISSIONE_MAN, NULL IM_MANDATO, NULL IM_TOTALE_NC, NULL CD_TIPO_CONTRATTO, DECODE(ESIGIBILITA_IVA, 'S', 'Y', 'D', 'Y','N') ESIGIBILITA_IVA
